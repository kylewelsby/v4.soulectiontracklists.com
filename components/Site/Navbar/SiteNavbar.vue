    )
      SiteNavSearch()
      SiteIconButton(
        icon="menu"
        class="md:hidden border-opacity-25 dark:border-opacity-25"
        @click="toggle()"
      )
</template>
<script>
export default {
  data() {
    return {
      state: 'closed',
    }
  },
  methods: {
    toggle() {
      if (this.state === 'closed') {
        this.state = 'opened'
      } else {
        this.state = 'closed'
      }
    },
    close() {
      this.state = 'closed'
    },
  },
}
</script>
